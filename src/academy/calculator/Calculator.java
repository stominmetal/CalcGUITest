package academy.calculator;

/**
 * Calculator
 * @author izosa
 */
public class Calculator {

    public Calculator() {
    }
    
    /**
     * Add 2 numbers
     * @param a
     * @param b
     * @return Double
     */
    public double addition(Double a, Double b){
        return a + b;
    }
    
    /**
     * Subtract 2 numbers
     * @param a
     * @param b
     * @return Double
     */
    public double subtraction(Double a, Double b){
        return a - b;
    }
    
    /**
     * Multiply 2 numbers
     * @param a
     * @param b
     * @return Double
     */
    public double multiplication(Double a, Double b){
        return a * b;
    }
    
    
    /**
     * Divide 2 numbers
     * @param a
     * @param b
     * @return Double
     */
    public double division(Double a, Double b){
        return a / b;
    }
    
}
